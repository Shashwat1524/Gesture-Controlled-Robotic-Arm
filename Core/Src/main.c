void set_servo_angle(TIM_HandleTypeDef *htim, uint32_t Channel, uint8_t angle) {
    // 0° => 0.5ms pulse, 180° => 2.5ms pulse
    uint16_t min_pulse = 500;    // 0.5ms = 500us
    uint16_t max_pulse = 2500;   // 2.5ms = 2500us
    uint16_t pulse = min_pulse + (angle * (max_pulse - min_pulse)) / 180;

    __HAL_TIM_SET_COMPARE(htim, Channel, pulse);
}

void set_gripper_position(uint8_t percent) {
    // percent: 0 (fully closed) to 100 (fully open)
    uint16_t min_pulse = 700;    // closed
    uint16_t max_pulse = 1800;   // open

    if (percent > 100) percent = 100;  // clamp to 100%

    uint16_t pulse = min_pulse + (percent * (max_pulse - min_pulse)) / 100;

    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, pulse);
}
void update_all_servos(uint8_t servo_vals[6]) {
    // servo_vals[0] = base
    // servo_vals[1] = shoulder
    // servo_vals[2] = elbow
    // servo_vals[3] = wrist
    // servo_vals[4] = wrist_rotate
    // servo_vals[5] = gripper (as percentage)

    set_servo_angle(&htim2, TIM_CHANNEL_1, servo_vals[0]); // base
    set_servo_angle(&htim1, TIM_CHANNEL_1, servo_vals[1]); // shoulder

    set_servo_angle(&htim1, TIM_CHANNEL_2, servo_vals[2]); // elbow

    set_servo_angle(&htim1, TIM_CHANNEL_3, servo_vals[3]); // wrist

    set_servo_angle(&htim1, TIM_CHANNEL_4, servo_vals[4]); // wrist rotate
//    set_gripper_position(servo_vals[5]);                   // gripper
}


